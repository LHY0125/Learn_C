#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include <math.h>
#include <stdbool.h>

// !本地变量（也叫局部变量）
/*
    定义：所有在函数内定义的变量都是本地变量
    生存期：变量从定义开始，到函数结束
    作用域：在什么范围内可以访问这个变量（这个变量可以起作用）
    对于本地变量，范围是函数大括号{}内
    特点：
    1.不同函数中的同名变量互不干扰
    2.外层变量会被内层同名变量暂时隐藏（变量覆盖）
    3.未初始化时值为随机数（栈内存特性）
*/
   
int main()
{
    // 例子
    int a = 10;
    {
        int a = 20;
        printf("a = %d\n", a);  // 输出20，内层变量a覆盖了外层变量a
    }
    printf("a = %d\n", a);      // 输出10，内层变量a被隐藏了

    return 0;
}
